// Basic Java Program
public class Main {
    public static void main(String[] args) {
        // Print a message
        System.out.println("Hello, World!");

        // Declare and initialize variables
        int a = 10;
        int b = 20;

        // Perform addition
        int sum = a + b;

        // Display the result
        System.out.println("Sum of " + a + " and " + b + " is: " + sum);
    }
}
